/* O JS é uma linguagem fracamente tipada e dinâmica 

- Variávies não precisam ter um tipo previamente definido
- Podemos mudar o contéudo da variável
*/


let clima = true // valor boolean
clima = "" // string

console.log(typeof clima)

//O Javascript é totalmente dinamico, acima colocamos um valor na variavel boolean e em baixo atualizamos a variavel para string, isso é dinamico.

// já se a variavel for do tipo const , o valor sempre será constante e não tem como alterar e atribuir novos valores depois, o let e o var permitem alterações e atribuições de valores depois

